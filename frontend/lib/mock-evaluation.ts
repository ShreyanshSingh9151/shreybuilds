function buildSummary(scenarios: BenchmarkScenario[]): EvaluationSummary {
  const total = scenarios.length || 1;
  const totalLatency = scenarios.reduce((sum, item) => sum + item.latency_ms, 0);
  const totalCost = scenarios.reduce((sum, item) => sum + item.cost_usd, 0);
  const totalSavings = scenarios.reduce((sum, item) => sum + item.savings_usd, 0);
  const passes = scenarios.filter((item) => item.passed).length;
  const providerList = Array.from(new Set(scenarios.map((item) => item.provider)));

  return {
    average_latency_ms: totalLatency / total,
    average_cost_usd: totalCost / total,
    total_savings_usd: totalSavings,
    pass_rate: passes / total,
    provider_coverage: providerList.map((provider) => provider.toUpperCase()).join(" + "),
    provider_count: providerList.length,
    total_benchmarks: scenarios.length
  };
}

export const mockEvaluationSummary = buildSummary(mockEvaluationScenarios);
